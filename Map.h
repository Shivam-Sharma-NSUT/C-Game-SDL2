SDL_Texture *water,*dirt,*grass;
SDL_Rect dr1;

bool LoadMap(){
    water=SDL_CreateTextureFromSurface(renderer,IMG_Load("assets/water.png"));
    dirt=SDL_CreateTextureFromSurface(renderer,IMG_Load("assets/dirt.png"));
    grass=SDL_CreateTextureFromSurface(renderer,IMG_Load("assets/grass.png"));
    return false;
}

void DrawMap(int x,int y){
    static bool flag=true;
    if( flag ) flag = LoadMap();
    dr1.w=dr1.h=64;
    dr1.x=dr1.y=0;
    for(int i=0;i<40;i++){
        for(int j=0;j<40;j++){
            dr1.x=64*j;
            dr1.y=64*i;
            if(dr1.x<=x+900&&dr1.x>=x-700&&dr1.y<=y+450&&dr1.y>=y-400){
                dr1.x-=x-500;
                dr1.y-=y-200;
                switch(Map[i][j]){
                    case 0: SDL_RenderCopy(renderer,water,NULL,&dr1);
                        break;
                    case 1: SDL_RenderCopy(renderer,dirt,NULL,&dr1);
                        break;
                    case 2: SDL_RenderCopy(renderer,grass,NULL,&dr1);
                        break;
                }
            }
        }
    }
}

